name="John"
age=20
language="Python"
hours=3

# John is 20 years old. He is learning Python for 3 hours a day.

print(name, "is", age, "years old. He is learning", language, "for", hours, "hours a day.") # Default separator is space

# Using f-string for better readability
print(f"{name} is {age} years old. He is learning {language} for {hours} hours a day.") # Using f-string for better readability
