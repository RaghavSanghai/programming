# \n - Newline
# \t - Tab
# \\ - Backslash
# \' - Single Quote
# \" - Double Quote
# Example usage of escape sequences
print("Hello, World!\nWelcome to Python programming.")
print("This is a tab:\tSee the space after the tab?")
print("This is a backslash: \\")
print("She said, \"Hello!\"")
print('It\'s a nice day!')
